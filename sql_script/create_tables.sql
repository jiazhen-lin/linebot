CREATE TABLE IF NOT EXISTS User (
    userID VARCHAR(64) NOT NULL PRIMARY KEY,
    userType VARCHAR(32) NOT NULL,
    name VARCHAR(64)
) ENGINE=InnoDB CHARACTER SET=utf8mb4 COLLATE=utf8mb4_general_ci;

CREATE TABLE IF NOT EXISTS Accounting (
    ID INT UNSIGNED NOT NULL PRIMARY KEY AUTO_INCREMENT,
    userID VARCHAR(64) NOT NULL,
    category VARCHAR(64) NOT NULL,
    createdTime DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'The created time of this record',
    accountingTime DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'The time of spending',
    price DECIMAL(19, 4) NOT NULL,
    detail VARCHAR(128) DEFAULT NULL
) ENGINE=InnoDB CHARACTER SET=utf8mb4 COLLATE=utf8mb4_general_ci;